
function getArea( length, width ){

     let area = length * width ;

     console.log( `The area of Rectangle is ${area} `);
}
function getAreaS( body ){

     let areas = body * body ;

     console.log( `The area of Square is ${areas} `);
}
function getAreaT( ground, height ){

     let areaT = 1/2 * ground * height ;

     console.log( `The area of Triangle is ${areaT} `);
}
